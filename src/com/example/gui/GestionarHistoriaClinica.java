    public void CargarDatosAnimales() {
        ArrayList<Animal> registro = regAnimales.getListaAnimales();
        for (Animal regis : registro) {
            SeleccionAnimal.addItem(regis.getCodigo() + regis.getNombre());
        }
    }

    public void CargarDatosUsuarios() {
        ArrayList<Usuarios> registroUsuarios = regUsuarios.getListaUsuarios();
        for (Usuarios reg : registroUsuarios) {
            SeleccionUsuario.addItem(reg.getNombres() + " " + reg.getDocumento());
        }
    }

