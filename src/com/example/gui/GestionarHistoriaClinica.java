    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        HistoriaClinica historia = new HistoriaClinica();
        historia.setFecha(Fecha.getText());
        historia.setConsecutivo(consecutivo.getText());
        
        if (SeleccionAnimal.getSelectedItem().equals("Seleccione un animal")){
            JOptionPane.showMessageDialog(null, "Escoja una opcion valida", "Animal no seleccionado", JOptionPane.WARNING_MESSAGE);
               
        }else if(procedimiento.getSelectedItem().equals("Seleccione un Procedimiento")){
            JOptionPane.showMessageDialog(null, "Escoja una opcion valida", "Procedimiento no seleccionado", JOptionPane.WARNING_MESSAGE);
        
        }else if(SeleccionUsuario.getSelectedItem().equals("Seleccione un Usuario")){
            JOptionPane.showMessageDialog(null, "Escoja una opcion valida", "Usuario no seleccionado", JOptionPane.WARNING_MESSAGE);
        }else{
            
        
        historia.setAnimal(SeleccionAnimal.getSelectedItem().toString());
        historia.setProcedimiento(procedimiento.getSelectedItem().toString());
        historia.setUsuario(SeleccionUsuario.getSelectedItem().toString());
        historia.setObservaciones(observaciones.getText());
        listaHistoria.add(historia);
        conse = conse + 1;
        consecutivo.setText(Integer.toString(conse));
        JOptionPane.showMessageDialog(null, "Novedad agregada Exitosamente");
        
        SeleccionAnimal.setSelectedIndex(0);
        procedimiento.setSelectedIndex(0);
        SeleccionUsuario.setSelectedIndex(0);
        observaciones.setText("");
        }
        System.out.println(historia);
    }//GEN-LAST:event_jButton2ActionPerformed

